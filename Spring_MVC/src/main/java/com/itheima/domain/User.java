package com.itheima.domain;

public class User {
    String name;
    int age;

    public void setUserName(String name) {
        this.name = name;
    }

    public String getName() {
        return name;
    }

    public void setAge(int age) {
        this.age = age;
    }

    public int getAge() {
        return age;
    }
}
